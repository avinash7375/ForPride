#include <stdio.h>

int main() {
    int a = 10;           // Integer
    float b = 5.5;        // Float
    char c = 'A';         // Character
    double d = 12.3456;   // Double
    
    printf("Integer: %d\n", a);
    printf("Float: %.2f\n", b);
    printf("Character: %c\n", c);
    printf("Double: %.4lf\n", d);
    
    return 0;
}
